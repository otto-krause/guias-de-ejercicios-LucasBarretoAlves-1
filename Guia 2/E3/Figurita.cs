namespace E3
{
    public class Figurita
    {
        public string nombre;
        public string posicion;
        public string pais;
        public int numero;
        public Figurita(string nombre, string posicion, string pais, int numero)
        {
            this.nombre=nombre;
            this.posicion=posicion;
            this.pais=pais;
            this.numero=numero;
        }
    }
}